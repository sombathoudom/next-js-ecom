export default function page() {
  const array = [
    {
      label: "Pending",
      value: "pending",
    },
    {
      label: "Approved",
      value: "Approved",
    },
  ];
  return (
    <div>
      <ButtonWithLink href={ROUTES.admin.users.create.path} text="Add New" />
      <DataTable
        columns={columns}
        data={payments}
        filters={[
          {
            columnId: "status",
            title: "Status",
            options: array,
          },
        ]}
      />
    </div>
  );
}
